public class Aritmeticos {
    public static void main(String[] args) {
            System.out.println(2+3);
            
            var x = 35.65;
            double y = 2.3;
            
            System.out.println(x + y);
            System.out.println(x - y);
            System.out.println(x * y);
            System.out.println(x /y );
            
            int a = 9; 
            int b = 4;
            
            
            System.out.println(a + b);
            System.out.println(a - b);
            System.out.println(a * b);
            System.out.println(a / b);
            System.out.println(a / (double)b);
            
            // modulo (rsto da divição )
            System.out.println(a % b);
                    
    }
        
}
